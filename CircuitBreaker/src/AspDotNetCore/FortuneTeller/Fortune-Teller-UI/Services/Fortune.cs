using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace Fortune_Teller_UI.Services
{
    public class Fortune
    {
        public int Id { get; set; }
        public string Text { get; set; }

        public override string ToString()
        {
            return "[" + Id + "," + Text + "]";
        }
    }
}
